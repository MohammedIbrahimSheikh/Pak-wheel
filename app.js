//function for creating cards
function createCard(company,category,n){
    return `
    <div class= "card mx-1 my-2">
        <div d-flex>
            <img class="card-img-top" src="${cars[company][category][n].img}" alt="car-image">
        </div>
        <div class = "container">
            <h6 class = " card-title mx-auto my-1">${cars[company][category][n].name.toUpperCase()}</h6>
            <span class = "price d-block mx-auto ">${cars[company][category][n].price.toUpperCase()}</span>
            <span class = "d-block mx-auo ">${cars[company][category][n].location.toUpperCase()}</span>
        </div>
    </div>`;
}
//function for displaying all cars
function displayAllCars(){
    for (i in cars){
        for (j in cars[i] ){
            for (k in cars[i][j]){
                let cards = createCard(i,j,k)
                body.innerHTML += cards;        
            }
        }
    }
}
//for filtering cars
function displaySelectedCars(company,category){
    body.innerHTML = "";
    for (i in cars[company][category]){
        let cards = createCard(company,category,i)
        body.innerHTML += cards;        
    }
}
//for real time searching
function search(){
    body.innerHTML = "";
    let flag = false;
    let searchItem = document.getElementById("search-item").value.toLowerCase().trim();
    for (i in cars){
        for (j in cars[i] ){
            for (k in cars[i][j]){
                if (cars[i][j][k].name.toLowerCase().includes(searchItem)){
                    flag = true;
                    let cards = createCard(i,j,k)
                    body.innerHTML += cards;        
                }
            }
        }
    }
    if(!flag){
        body.innerHTML = "<div class='h1'>Couldn't Find Your Search Result...!!!</div>"
    }
}
// Trigger the displayAllCars() function when the page loads
window.addEventListener("load",displayAllCars());