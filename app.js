for (var i = 0; i < navLinks.length -1; i++){
    navLinks[i+1].innerHTML = mainObjkeys[i]
    let dropDownitems = Object.keys(cars[mainObjkeys[i]])
    for(var j = 0; j < dropDownitems.length ;j++){
        document.getElementsByClassName("dropdown-menu")[i].children[j].childNodes[0].innerText = dropDownitems[j]
    }}
